class Solution{
public:	
	// calculate the maximum sum with out adjacent
	int findMaxSum(int *arr, int n) {
	    // code here
	    	    int next1 = 0, next2 = 0;
	    for(int i=n-1;i>=0;i--){
	        int cur = max(next1,arr[i]+next2);
	        next2=next1;
	        next1=cur;
	    }
	    return next1;

	}
};

